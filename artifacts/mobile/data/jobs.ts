export function getFilteredJobs(
  jobs: Job[],
  state: string,
  jobTypes: JobType[],
  searchQuery: string = "",
  filterType?: JobType | null
): Job[] {
  return jobs.filter((job) => {
    const stateMatch =
      job.state === "All India" ||
      job.state === state ||
      state === "All India";
    const typeMatch =
      jobTypes.length === 0 ||
      jobTypes.includes(job.jobType) ||
      (filterType ? job.jobType === filterType : true);
    const filterTypeMatch = !filterType || job.jobType === filterType;
    const searchMatch =
      !searchQuery ||
      job.title.toLowerCase().includes(searchQuery.toLowerCase()) ||
      job.organization.toLowerCase().includes(searchQuery.toLowerCase()) ||
      job.jobType.toLowerCase().includes(searchQuery.toLowerCase());
    return stateMatch && typeMatch && filterTypeMatch && searchMatch;
  });
}
